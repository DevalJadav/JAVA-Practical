class pyramid2
{					
	public static void main(String args[])       
	{                                                                     
		for(int i=1;i<=5;i++)//num of row
		{ 
		   
		    for(int j=0;j<5-i;j++)//num of columns
		     {
			System.out.print(" ");
		     }
			System.out.println("*");
		} 
		
	}
}